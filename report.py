    a('## Почему выбраны технологии и способы взаимодействия')
    a('')
    a('В этом разделе каждая технология объясняется не как отдельный термин, а как решение для конкретного шага процесса: почему она подходит, почему не выбран другой способ и какие условия обязательны, чтобы решение было безопасным.')
    a('')
    a('| Шаг | Технология / способ | Почему выбрано | Почему не другой вариант | Обязательные условия |')
