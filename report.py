    a('**Этап 2 — усиление перед промышленным запуском:** закройте пункты со статусами «не указано» и «требует проверки» чек-листа: контракты, '
      'наблюдаемость, повторная обработка, срок хранения, сверка, нагрузочный тест.')
    a('')
    a('**Этап 3 — эксплуатация:** подготовьте инструкцию разбора инцидентов, регулярную сверку, обучение поддержки и '
      'проверку повторной обработки и отката на регламентной основе.')
    a('')
